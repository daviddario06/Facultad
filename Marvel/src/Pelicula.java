
public class Pelicula {
	
	private String nombre;
	private int emision;
	private int cronologico;
	private String estado;
	
	public int getEmision() {
		return emision;
	}
	
	public int getCronologico() {
		return cronologico;
	}
	
	public String getNombre() {
		return nombre;
	}

	public String getEstado() {
		return estado;
	}
}
